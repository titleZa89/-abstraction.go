package main

import "fmt"

// อินเตอร์เฟซ สัตว์
type สัตว์ interface {
	ส่งเสียง()
}

// ยีราฟ
type ยีราฟ struct {
	ความสูง float64
	คอยาว   bool
}

// เสือ
type เสือ struct {
	มีลายเสือ bool
	ความเร็ว  int
}

// เมธอดของยีราฟ
func (ย ยีราฟ) ส่งเสียง() {
	fmt.Println("เสียงยีราฟ: ฮืมมม... ความสูง:", ย.ความสูง, "เมตร คอยาว:", ย.คอยาว)
}

// เมธอดของเสือ
func (ส เสือ) ส่งเสียง() {
	fmt.Println("เสียงเสือ: แง๊วววว! มีลาย:", ส.มีลายเสือ, "ความเร็ว:", ส.ความเร็ว, "กม./ชม.")
}

func main() {
	// สร้างยีราฟ
	ย := ยีราฟ{
		ความสูง: 5.2,
		คอยาว:   true,
	}

	// สร้างเสือ
	ส := เสือ{
		มีลายเสือ: true,
		ความเร็ว:   65,
	}

	// เก็บลง slice ของ interface
	สวนสัตว์ := []สัตว์{ย, ส}

	// ให้สัตว์แต่ละตัวส่งเสียง
	for _, สัตว์ตัวหนึ่ง := range สวนสัตว์ {
		สัตว์ตัวหนึ่ง.ส่งเสียง()
	}
}
